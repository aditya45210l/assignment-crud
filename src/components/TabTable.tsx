"use client";

import { ScrollArea } from "@/components/ui/scroll-area";
import { Tabs, TabsContent, TabsList, TabsTrigger } from "@/components/ui/tabs";
import { useUserName } from "@/hooks/useUserName";
import { client } from "@/lib/client";
import { useQuery } from "@tanstack/react-query";
import { Mail, MessageSquareText } from "lucide-react";

interface TableRow {
    customId: string;
    mobileNumber?: string;
    message: string;

    emailAddress?: string;
    subject?: string;
    [key: string]: unknown;
}

const TableComponent = ({ data, columns }: { data: TableRow[], columns: string[] }) => (
    <div className="rounded-lg border bg-card shadow-sm">
        <ScrollArea className="h-[400px]">
            <table className="w-full">
                <thead className="border-b">
                    <tr>
                        {columns.map(col => (
                            <th key={col} className="px-4 py-3 text-left font-medium text-sm">{col}</th>
                        ))}
                    </tr>
                </thead>
                <tbody>
                    {data?.map(item => (
                        <tr className="border-b" key={item.customId + item.message}>
                            {columns.map(col => (
                                <td key={col} className="px-4 py-3 text-sm text-muted-foreground">{String(item[col])}</td>
                            ))}
                        </tr>
                    ))}
                </tbody>
            </table>
        </ScrollArea>
    </div>
)

const TabTable = () => {
    const [userName] = useUserName();
    console.log("userName: ", userName);
    const { data: whatsappData } = useQuery({
        queryKey: ['whatsapp', userName],
        queryFn: async () => {
            const data = (await client.send.whatsapp.get({ query: { customId: userName } }));
            if (!data) {
                console.log("error");
            }
            return data.data;
        }, refetchOnMount: "always", enabled: !!userName,

    })

    const { data: emailData } = useQuery({
        queryKey: ['email', userName],
        queryFn: async () => {
            const data = await client.send.email.get({ query: { customId: userName } });
            return data.data;
        }, refetchOnMount: "always"
        ,
        enabled: !!userName,

    })

    const { data: smsData } = useQuery({
        queryKey: ['sms', userName],
        queryFn: async () => {
            const data = await client.send.sms.get({ query: { customId: userName } });
            return data.data;
        }, refetchOnMount: "always"
        , enabled: !!userName,

    })

    return (
        <Tabs className="w-full max-w-4xl" defaultValue="whatsapp">
            <TabsList className="grid grid-cols-3 w-full">
                <TabsTrigger value="whatsapp">
                    <svg role="img" viewBox="0 0 24 24" xmlns="http://www.w3.org/2000/svg"><title>WhatsApp</title><path d="M17.472 14.382c-.297-.149-1.758-.867-2.03-.967-.273-.099-.471-.148-.67.15-.197.297-.767.966-.94 1.164-.173.199-.347.223-.644.075-.297-.15-1.255-.463-2.39-1.475-.883-.788-1.48-1.761-1.653-2.059-.173-.297-.018-.458.13-.606.134-.133.298-.347.446-.52.149-.174.198-.298.298-.497.099-.198.05-.371-.025-.52-.075-.149-.669-1.612-.916-2.207-.242-.579-.487-.5-.669-.51-.173-.008-.371-.01-.57-.01-.198 0-.52.074-.792.372-.272.297-1.04 1.016-1.04 2.479 0 1.462 1.065 2.875 1.213 3.074.149.198 2.096 3.2 5.077 4.487.709.306 1.262.489 1.694.625.712.227 1.36.195 1.871.118.571-.085 1.758-.719 2.006-1.413.248-.694.248-1.289.173-1.413-.074-.124-.272-.198-.57-.347m-5.421 7.403h-.004a9.87 9.87 0 01-5.031-1.378l-.361-.214-3.741.982.998-3.648-.235-.374a9.86 9.86 0 01-1.51-5.26c.001-5.45 4.436-9.884 9.888-9.884 2.64 0 5.122 1.03 6.988 2.898a9.825 9.825 0 012.893 6.994c-.003 5.45-4.437 9.884-9.885 9.884m8.413-18.297A11.815 11.815 0 0012.05 0C5.495 0 .16 5.335.157 11.892c0 2.096.547 4.142 1.588 5.945L.057 24l6.305-1.654a11.882 11.882 0 005.683 1.448h.005c6.554 0 11.89-5.335 11.893-11.893a11.821 11.821 0 00-3.48-8.413Z" /></svg>
                    whatsapp
                </TabsTrigger>
                <TabsTrigger value="mail"><Mail /> Mails</TabsTrigger>
                <TabsTrigger value="sms"><MessageSquareText /> SMS</TabsTrigger>
            </TabsList>
            <TabsContent value="whatsapp">
                <TableComponent data={whatsappData || []} columns={["customId", "mobileNumber", "message"]} />
            </TabsContent>
            <TabsContent value="mail">
                <TableComponent data={emailData || []} columns={["customId", "emailAddress", "subject", "message"]} />
            </TabsContent>
            <TabsContent value="sms">
                <TableComponent data={smsData || []} columns={["customId", "mobileNumber", "message"]} />
            </TabsContent>
        </Tabs>
    )
}

export default TabTable;